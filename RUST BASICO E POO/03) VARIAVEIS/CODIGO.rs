fn main() {
    // Declarando uma variável 'idade' e atribuindo um valor a ela
    let idade = 30; // Rust pode inferir o tipo da variável automaticamente

    // Declarando uma variável 'nome' com um tipo explícito (String)
    let nome: String = "Alice".to_string();

    // Imprimindo as variáveis
    println!("Nome: {}", nome);
    println!("Idade: {}", idade);
}
